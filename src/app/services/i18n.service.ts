  constructor() {
    const saved = localStorage.getItem(this.storageKey);
    if (saved === 'en' || saved === 'fr') {
      this.language = saved;
    }
  }

  get currentLanguage(): Language {
    return this.language;
  }

  setLanguage(language: Language): void {
    this.language = language;
    localStorage.setItem(this.storageKey, language);
  }

  t(key: string): string {
    return this.dictionary[this.language][key] ?? this.dictionary.en[key] ?? key;
  }

  enum(prefix: string, value: string | null | undefined, fallback?: string): string {
    const target = (value || fallback || '').trim();
    if (!target) {
      return '';
    }
    return this.t(`${prefix}_${target.toLowerCase()}`);
  }

  formatCurrency(amount: number | null | undefined): string {
    const safeAmount = Number(amount ?? 0);

    if (this.language === 'fr') {
      const fcfaAmount = Math.round(safeAmount * 500);
      const formattedFcfa = new Intl.NumberFormat('en-US', {
        maximumFractionDigits: 0,
      })
        .format(fcfaAmount)
        .replace(/,/g, '.');
      return `${formattedFcfa} FCFA`;
    }

    return new Intl.NumberFormat('en-US', {
      style: 'currency',
      currency: 'USD',
      minimumFractionDigits: 2,
      maximumFractionDigits: 2,
    }).format(safeAmount);
  }

  toDisplayAmount(amount: number | null | undefined): number {
    const safeAmount = Number(amount ?? 0);
    if (this.language === 'fr') {
      return Math.round(safeAmount * 500);
    }
    return safeAmount;
  }

  toStorageAmount(amount: number | null | undefined): number {
    const safeAmount = Number(amount ?? 0);
    if (this.language === 'fr') {
      return Math.round((safeAmount / 500) * 100) / 100;
    }
    return safeAmount;
  }
